let valor = 7;
//quando for usar && todas as informações tem que ser verdadeiras,
// se uma for falsa o resto todo é falso (prova)
if (valor > 10 && valor < 20){
    console.log('Este valor é válido');
}
// || é "OU", nessa se uma das codições for falsa ele roda 
if (valor > 10 || valor < 20){
    console.log('Este valor é válido');
}
